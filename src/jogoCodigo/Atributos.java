package jogoCodigo;

public abstract class Atributos {
    protected int hp;
    protected int forca;
    protected int inteligencia;
    protected int maxHP;

    public int getHP(){ return hp; }
    public int getForca(){  return forca; }
    public int getInteligencia(){  return inteligencia; }
    public int getMaxHP(){ return maxHP; }
}
